function createPerson(name, surname, age, eyeColor, adress ){
    return {
        name: name,
        surname:surname,
        age:age,
        eyeColor:eyeColor,
        adress:adress
    }
}

console.log(createPerson("A-7", "Simnon", 12, "blue", "tr7"))
console.log(createPerson("I-9", "Simnon", 14, "brown", "tr7"))
console.log(createPerson("Epsilon", "Simnon", 19, "green", "tr7"))
console.log(createPerson("X", "Simnon", 34, "blue", "tr7"))
console.log(createPerson("Tr-E", "Simnon", 8, "brown", "tr7"))